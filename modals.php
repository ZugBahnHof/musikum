<div id="klarinette" class="modal modal-fixed-footer">
  <div class="modal-content">
    <h1 class="center <?=$site_color_text?>">Klarinette</h1>
      <p class="flow-text">
        Die Klarinette gehört zu den Holzblasinstrumenten. Der Klang ist sehr variabel, man kann dem Instrument äußerst leise, weiche Töne entlocken, aber schrill und fetzig ist auch kein Problem. Mit der Klarinette ist man in jeder Stilrichtung zuhause! Den Unterricht beginnen kann man ab ca. 8 Jahren. Sollten die Hände noch zu klein sein, kann man mit einer kleineren Klarinette anfangen, bei der die Tonlöcher näher aneinander liegen. Viele Erwachsene, die ein Instrument erlernen möchten, entscheiden sich für die Klarinette.
      </p>
