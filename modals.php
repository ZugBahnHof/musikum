<div id="saxophon" class="modal modal-fixed-footer">
  <div class="modal-content">
    <h1 class="center <?=$site_color_text?>">Saxophon</h1>
      <p class="flow-text">
        Das Saxophon gehört zur Familie der Holzblasinstrumente, weil es mit einem Holzblättchen gespielt wird und so ähnlich gebaut ist wie andere Holzblasinstrumente, z.B. die Klarinette.
      </p>
      <p class="flow-text">
        Die vier häufigsten Saxophontypen sind  Sopran-, Alt-, Tenor- und Baritonsaxophon.
      </p>
      <p class="flow-text">
        Zum  Anfangen eignen sich am besten Alt- und Tenorsaxophon.
      </p>
      <p class="flow-text">
        Wegen des Gewichtes beginnen Kinder am besten mit einem Altsaxophon.
      </p>
      <p class="flow-text">
        Saxophon lernen können alle, die schon die zweiten - oder dritten - oberen Schneidezähne haben und deren Körper groß und kräftig genug ist, das Instrument mit einem Nacken- oder Rückengurt zu halten.
      </p>
